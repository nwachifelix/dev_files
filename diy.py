# This an if-else statement code that test whether an alien's colour is green and further prints out a message.  

alien_colour = 'green'

if alien_colour == 'green':
     print('\nYou have just earned 5 points for shooting down the alien. ')
     print('Congratulations!\t\n ')
print('This is the simplest kind of "if-statement" with one test and one action.\n ')

alien_colour = 'green'
if alien_colour != 'green':
    print('Game over. ')
else:
     print('You just earned 0 points. ')
     print('You can try again.\n ')
print('The "else statement" allows you make an action when the conditional test fails. ')